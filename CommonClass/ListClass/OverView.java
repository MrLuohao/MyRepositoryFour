package CommonClass.ListClass;

public class OverView {
    /*
    1.list集合中允许有重复的元素并且有先后放入次序
    2.该集合的是实现类主要有：ArrayList、LinkedList、Stack类、Vector类
    3.其中ArrayList类的底层是采用动态数组进行数据管理的，支持下标访问、增删元素不方便
    4.LinkedList类的底层是采用双向链表进行数据管理的，访问不方便，增删元素方便
    5.ArrayList和LinkedList的方法在逻辑上完全一样，只是性能上有一定的差别，ArrayList更适合于访问而LinkedList更适合插入和删除；在性能要求不是特别苛刻的情况下可以忽略这个差别
    6.Stack类的底层是采用动态数组进行数据管理的，该类主要用于描述一种具有后进先出特征的数据结构，叫做栈
    7.其中Vector类的底层采用动态数组进行数据管理的，该类属于线程安全的类，效率比较低
    8.ArrayList属于非线程的类，不安全但是效率高，默认内存扩容1.5倍；Vector属于线程安全的类，效率低，默认内存扩容2倍（开发中基本不用）
     */
}
